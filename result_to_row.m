function row = result_to_row(res, scenario_name)
% 把单场景结果整理成表格一行 (包含跨区套利收益)

    row = table( ...
        string(scenario_name), ...
        res.Obj_total, ...
        res.C_th, res.C_csp, res.C_grid, ...
        res.C_shift, res.C_DR_service, res.C_DR_short, ...
        res.C_curt, res.C_es, ...
        res.Savings_trans, ... % [核心修复] 将提取的套利收益存入表格变量
        res.C_qos, res.C_migrate_latency, res.C_delay, res.C_rebound, ... % QoS业务损失成本分解
        res.E_shift, res.E_up, res.E_down, res.E_cut, ...
        res.E_up_short, res.E_down_short, ...
        res.UpResponseRate, res.DownResponseRate, ...
        res.E_grid, res.E_curt, ...
        res.DCPeakValley, res.SystemNetLoadPeakValley, ...
        res.RenewUtilization, res.ThermalPeakValley, ...
        res.DCStd, res.SystemNetLoadStd, res.StorageEquivalentCycles, ...
        res.TaskTotalDemand, res.TaskTotalServed, ...
        res.TaskCompletionRate, res.TaskCurtailRatio, ...
        res.GreenPowerForDC, res.GreenPowerSupportRatio, ...
        res.DCRenewMatchIndex, res.RenewableToDCShare, ...
        res.TaskShiftRatio, res.TaskResponseRatio, ...
        res.ComputeEnergyIntensity, ...
        'VariableNames', { ...
        'Scenario', ...
        'TotalCost', ...
        'ThermalCost','CSPCost','GridCost', ...
        'ShiftCost','DRServiceCost','DRShortCost', ...
        'CurtailCost','StorageCost', ...
        'Savings_trans', ... % [核心修复] 定义变量名以供 main 识别
        'QosCost','MigrateLatencyCost','DelayCost','ReboundCost', ... % QoS业务损失成本分项
        'E_shift','E_up','E_down','E_cut', ...
        'E_up_short','E_down_short', ...
        'UpRespRate','DownRespRate', ...
        'E_grid','E_curt', ...
        'DCPeakValley','SystemNetLoadPeakValley', ...
        'RenewUtilization','ThermalPeakValley', ...
        'DCStd','SystemNetLoadStd','StorageCycles', ...
        'TaskDemand','TaskServed', ...
        'TaskCompletionRate','TaskCurtailRatio', ...
        'GreenPowerForDC','GreenPowerSupportRatio', ...
        'DCRenewMatchIndex','RenewableToDCShare', ...
        'TaskShiftRatio','TaskResponseRatio', ...
        'ComputeEnergyIntensity'});
end
